def get_lab_service_by_number(number):
    """Get a lab service tuple by its number."""
    for svc in LAB_SERVICES:
        if svc[0] == number:
            return svc
    return None


def search_lab_services(query, number_query=''):
    """Search lab services by number and/or name (case-insensitive).

    Args:
        query: Text to search in service name
        number_query: Number to match against service number

    Both can be used simultaneously for combined filtering.
    """
    query = query.strip().lower()
    number_query = number_query.strip()
    results = []
    for num, name, price, *rest in LAB_SERVICES:
        name_match = True
        num_match = True
        if query:
            # If query is purely digits and no separate number_query, search by number
            if query.isdigit() and not number_query:
                num_match = str(num).startswith(query)
                name_match = True  # no name filter
            else:
                name_match = query in name.lower()
        if number_query:
            num_match = str(num).startswith(number_query)
        if name_match and num_match:
            results.append((num, name, price, *rest))
    return results
